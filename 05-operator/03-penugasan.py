a //= 6
print('a //= 6 -> ', a)

a **= 1
print('a **= 1 -> ', a)


a &= 2
print('a &= 2 -> ', a)

a |= 3
print('a |= 3 -> ', a)

a ^= 4
print('a ^= 4 -> ', a)

a >>= 4
print('a >>= 4 -> ', a)

a <<= 2
